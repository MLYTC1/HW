def switch_it_up(num):
    if num == 0:
        return "Zero"
    if num == 1:
        return "One"
    if num == 2:
        return "Two"
    if num == 3:
        return "Three"
    if num == 4:
        return "Four"
    if num == 5:
        return "Five"
    if num == 6:
        return "Six"
    if num == 7:
        return "Seven"
    if num == 8:
        return "Eight"
    if num == 9:
        return "Nine"